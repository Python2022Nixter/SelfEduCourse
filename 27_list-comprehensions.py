N = 6
a = [0] * N
for i in range(N):
    a[i] = i ** 2
print(a)

# --------------------------------------------------
# List comprehension
# --------------------------------------------------
a = [i ** 2 for i in range(N)]
print(a)

# --------------------------------------------------
# [<способ формирования значения> for <имя переменной> in <итеррированный обьект>]
# --------------------------------------------------
a = [1 for i in range(N)]
print(a)

a = [x % 4 == 0 for x in range(N)]
print(a)

a = [0.5 * x + 1 for x in range(N)]
print(a)

