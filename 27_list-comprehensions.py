# --------------------------------------------------
d = [x for x in range(-5, 5) if x % 2 == 0]
print(d)

cities = ["Москва", "Санкт-Петербург", "Новосибирск", "Екатеринбург"]
a = [city for city in cities if len(city) > 7]
print(a)

